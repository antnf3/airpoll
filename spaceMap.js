// - 하늘상태(SKY) 코드 : 맑음(1), 구름조금(2), 구름많음(3), 흐림(4)
// - 강수형태(PTY) 코드 : 없음(0), 비(1), 비/눈(2), 눈(3)
//   여기서 비/눈은 비와 눈이 섞여 오는 것을 의미 (진눈개비)
// - 낙뢰코드(LGT) 정보
//   낙뢰(초단기예보) :확률없음(0), 낮음(1), 보통(2), 높음(3)

const spaceMap = {
  getSpaceName: (nx, ny) => {
    const selectedSpaceMap = SPACE.filter(space => {
      return space.x == nx && space.y == ny;
    });
    if (selectedSpaceMap.length > 0) {
      return selectedSpaceMap[0].secondGrade || selectedSpaceMap[0].firstGrade;
    } else {
      return "없음";
    }
  },

  // 하늘상태
  getSkyStatus: status => {
    return SKY[status];
  },
  // 강수형태
  getPtyStatus: status => {
    return PTY[status];
  },
  // 낙뢰
  getLgtStatus: status => {
    return LGT[status];
  },

  getSkyIcon: (sky, pty) => {
    let skyIcon = "";
    if (pty !== 0) {
      if (pty === 1) {
        // 비
        skyIcon = "weather-pouring";
      } else if (pty === 2) {
        // 비/눈
        skyIcon = "weather-snowy-rainy";
      } else if (pty === 3) {
        // 눈
        skyIcon = "weather-snowy";
      }
    } else {
      if (sky === 1) {
        // 맑음
        skyIcon = "weather-sunny";
      } else if (sky === 2) {
        // 구름조금
        skyIcon = "weather-partlycloudy";
      } else if (sky === 3) {
        // 구름많음
        skyIcon = "weather-cloudy";
      } else if (sky === 4) {
        // 흐림
        skyIcon = "weather-cloudy";
      }
    }
    return skyIcon;
  }
};

export default spaceMap;
